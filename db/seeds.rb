Preparation time: 35 mins 
Ingredients:
 Wheat Flour - 2 cup's 
 Methi leaves - 1/2 cup (finely chopped)
  White sesame seeds - 1 tbsp 
  Turmeric powder - 1/2 tsp 
  Asafoetida powder - a pinch 
  Cumin powder - 1/2 tsp 
  Ginger Garlic paste - 2 tsp 
  Green chilly paste - 2 tsp 
  Butter - 2 tbsp (melted) 
  Curd - 3 tbsp's 
  Salt to taste 
  Water needed to knead the dough 
 Method: Wash the methi leaves and chop it finely. make sure you do not add stems of it. as it may not be easy while rolling.
  Mix all the ingredients mentioned above together. sprinkle water and knead the dough just we do for our chapathi dough. the dough may stick to your hands, because of curd in it. add little flour or grease your palms with oil while kneading. 
    Once the dough is kneaded, split the dough into lemon sized balls. it should make about 12 or 14 balls. roll the balls over little flour before you start to roll them. roll them out thin to about 6 inches in diameter. if it cracks while rolling, just patch it up with hands. and dust little flour then start to roll again.
  Heat a Skillet and cook the rolled theplas at high heat.drizzle oil to the both sides of the thepla and cook till brown spots appear on them. it takes about 1 to 2 minutes of time to cook each thepla. cook the other theplas the same way and stack them one above other. 
      Serve the methi theplas with curd and pickle.",
